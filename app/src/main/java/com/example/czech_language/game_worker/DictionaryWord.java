package com.example.czech_language.game_worker;

public class DictionaryWord {

    String czWord, ruWord, ruWordWrong;

    public String getCzWord() {
        return czWord;
    }

    public String getRuWord() {
        return ruWord;
    }

    public String getRuWordWrong() {
        return ruWordWrong;
    }

    public void setCzWord(String czWord) {
        this.czWord = czWord;
    }

    public void setRuWord(String ruWord) {
        this.ruWord = ruWord;
    }

    public void setRuWordWrong(String ruWordWrong) {
        this.ruWordWrong = ruWordWrong;
    }
}
